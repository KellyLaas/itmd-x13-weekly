#from 12.3 Hash Class
dict_array = []
dict_hash = {}

dict_array[0] = 'candle'
dict_array[1] = 'glasses'
dict_array[2] = 'toilet'
dict_array[3] = 'shaving'
dict_hash[shia-a] = 'candle'
dict_hash[shia-b] = 'glasses'
dict_hash[shia-c] = 'toilet'
dict_hash[shia-d] = 'shaving'

dict_array.each do |word|
  puts word
end

dict_hash.each do |c_word, word|
  puts "#{c_word}:  #{word}"
end
